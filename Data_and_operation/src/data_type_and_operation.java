public class data_type_and_operation {
    public static void main(StringApp[] args){
        System.out.println(6);//number
        System.out.println("six");//String문자열

        System.out.println("6");//String 6

        System.out.println(6+6);//12
        System.out.println("6"+"6");//66
        System.out.println(6*6);//36
//        System.out.println("6"*"6");//error

        System.out.println("1111".length());//str은 숫자 셀수 있다4
//        System.out.println(1111.length());//error int는 셀 수 없다

    }
}